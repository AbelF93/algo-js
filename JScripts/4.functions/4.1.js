/*This exercice demands to calculate the surface of a rectangle our a square, using a function.*/

function calcSurface (lenght, width) {/* a function called calcSurface with 'length' and 'width' with arguments.*/
    return lenght*width; /*we return to those arguments by multiplying them to calculate the surface.*/
}
/*calls the function to calcute the surface using selected values.*/
console.log(calcSurface(15,9));
console.log(calcSurface(6,1));
